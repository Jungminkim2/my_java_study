package icehs.science.chapter04;

public class DivisionEx {
	public static void main(String[] args) {
		int firstNum = 20;
		int secondNum = 7;
		int addResult = firstNum + secondNum;
		int subtractResult = firstNum - secondNum;
		int multiplyResult = firstNum * secondNum;
		int divideResult = firstNum /secondNum;
		int remainderResult = firstNum % secondNum;
		
		System.out.println("오챯叩 : "+ addResult);
		System.out.println("짋珝叩 : "+ subtractResult);
		System.out.println("썼챯叩 : "+ multiplyResult);
		System.out.println("씱얋챯叩 : "+ divideResult);
		System.out.println("씱쟵쵔 : "+ remainderResult);
	}
}
